    <h2>Constituencies and Polling Stations</h2> 
   <p>The National Electoral Board of Ethiopia (NEBE) is empowered by Article 7 of Proclamation No. 1133/2011 to establish polling constituencies and stations throughout the country. The constituencies established in this manner shall have the following powers and functions in accordance with Article 14 of Proclamation No. 1162/2011.</p>
      
     
   <h4>A constituency:</h4> 
<ul> 
    <li>According to the schedule set by the Board, distributes election materials to polling stations, and registers candidates, and issues ID cards to eligible candidates;</li>
     <li>Shall decide on complaints and grievances in accordance with the law;</li>
    <li>After collecting the results from the polling station, it shall identify the winners and announce provisional results to the public, fills in necessary forms and sends them to the Board;</li>
    <li>Transfers the list of representatives of the parties to the polling stations;</li>
    <li>Responds immediately to complaints that come from polling stations regarding the election process and results;</li>
    <li>Performs other functions as may be provided by the Board or the regional branch office.  </li>
    

</ul>





<p>
Similarly, a polling station that will be established by the Board shall have the following powers and functions in accordance with Article 16 of Proclamation No. 1162/2011.
</p>
<h4>A polling station: </h4>
<ul>
<li>Registers voters;</li>
<li>Receives and carefully handles election materials dispatched from the constituency;</li>
<li>Holds secret elections in accordance with the law;</li>
<li>Deals with grievances in accordance with the law;</li>
<li>Informs the public by conducting the vote count and posting the provisional results on the bulletin board;</li>
<li>Performs other functions as may be provided by the Board, the Regional Branch Office, or the constituency.</li>
</ul>


<p>
In addition, in accordance with Article 17 of Proclamation No. 1162/2011, the Board shall facilitate the participation of soldiers, civil servants, and their families living in camps far from their constituencies, students of higher education institutions, displaced persons, and inmates who have not been deprived of their right to vote by establishing special polling stations.
</p>
<h2>Electoral Cycle</h2>
<p>
An electoral cycle represents the different stages and processes in an electoral system, which encompasses all activities from preparation to an election and all the way through results verification and announcement, as well as grievance hearing and resolution after the election. Electoral cycle is divided into three main periods: pre-election period, election period, and post-election period.
</p>
<h4>Pre-election period</h4>
<p>
The pre-election period is the timeline encompassing all activities leading to Election Day. Major activities during this period include: registration of political parties, preparation of the legal frameworks, recruitment, training and assignment of election officials, constituency delimitation, voters registration, gender inclusion, media engagement, civic and voters education, establishment of polling stations, candidate registration, political campaigning, and time for mass media to broaden operations. This period is the time for the elections management body and key stakeholders to engage with and implement a wide range of activities to prepare for and improve the electoral process.
</p>
<h4>Election period</h4>
<p>
Based on the electoral cycle, the election period is the time where various important tasks are performed. These include: voting, counting votes, verification and announcement of results, as well as grievance hearing and resolution.
</p>
<h4>Post-election period </h4>
<p>
The post-election period is the period after an election is conducted and final results have been announced. This period involves post-election assessment and evaluation, constituency delimitation according to the legal framework, and assessing the Electoral Management Bodyâ€™s (EMB) strategic plans and readiness for future elections.
</p>

<h2>Voting</h2>
<P>
After citizens are registered as voters during the time set for voter registration, citizens will vote by going to the polling station where they are registered on the Election Day specified by the NEBE. 
</P>
<h4>Voting Process </h4>
<ul>
<li>The voting process begins at all polling stations at the same time.</li>
<li>On polling day, voters can vote from 6AM to 6PM. During this time period, voting will continue without interruption unless there are difficult circumstances beyond control.</li>
<li>Any voter who is present at the polling station will vote freely. </li>
<li>The secrecy of the ballot must always be ensured. Voters who need help while voting may enter the secret voting booth with their selected assistant. The assistant must not have had any of his/her rights stripped by law.  </li>


<li>Any voter can vote:</li>
<ul>
<li>at the polling station where he/she is registered as a voter;</li>
<li>if he/she is in possession of their voter ID card</li>
<li>if his/her name can be found on the electoral roll</li>
<li>when it has been confirmed that he/she has not yet voted.</li>

</ul>
<li>Any voter present at the polling station where he/she is registered must provide poll workers with identification documents in order to receive their permission to cast his/her vote.</li>
<li>People with disabilities, the elderly, parents with children and pregnant women will be given rights-of-way in the registration and voting processes.</li>

</ul>

<h3>The ethical principles that must be respected on the Election Day</h3>
<ul>
    <li>No one should be present within a 200m radius of the polling station while drunk, carrying a firearm or disrupting the peace in any other manner.</li>
    <li>Any person who is not a voter and does not have an identification card should not enter the polling station, nor should they interfere with or obstruct any electoral processes taking place in or outside the compound of the polling station.</li>
    <li>After casting a vote, the voter must immediately leave the polling station and its surrounding area.</li>
</ul>

